package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"go/types"
	"log"
	"os"
	"sort"
	"strings"

	"golang.org/x/tools/go/packages"

	"github.com/yandex/perforator/library/go/otel_semconv_forward/internal/versioncfg"
)

func main() {
	var upstream string
	var out string

	flag.StringVar(&upstream, "upstream", "", "upstream package import path")
	flag.StringVar(&out, "out", "semconv_gen.go", "output file")
	flag.Parse()

	if upstream == "" {
		resolved, err := versioncfg.ReadSemconvPackage("versions.mk")
		if err != nil {
			log.Fatalf("resolve upstream semconv package: %v", err)
		}
		upstream = resolved
	}

	cfg := &packages.Config{
		Mode: packages.NeedName | packages.NeedTypes | packages.NeedTypesInfo | packages.NeedImports | packages.NeedDeps,
	}
	pkgs, err := packages.Load(cfg, upstream)
	if err != nil {
		log.Fatalf("packages.Load: %v", err)
	}
	if packages.PrintErrors(pkgs) > 0 {
		log.Fatal("failed to load upstream package")
	}
	if len(pkgs) != 1 || pkgs[0].Types == nil {
		log.Fatal("upstream package not loaded with type info")
	}

	scope := pkgs[0].Types.Scope()
	names := scope.Names()
	sort.Strings(names)

	consts := make([]string, 0, len(names))
	vars := make([]string, 0, len(names))
	funcs := make([]string, 0, len(names))
	typeAliases := make([]string, 0, len(names))

	for _, name := range names {
		if !isExported(name) || name == "init" {
			continue
		}
		switch scope.Lookup(name).(type) {
		case *types.Const:
			consts = append(consts, name)
		case *types.Var:
			vars = append(vars, name)
		case *types.Func:
			funcs = append(funcs, name)
		case *types.TypeName:
			typeAliases = append(typeAliases, name)
		}
	}

	var b bytes.Buffer
	fmt.Fprintln(&b, "// Code generated by gensemconv; DO NOT EDIT.")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "package semconv")
	fmt.Fprintln(&b)
	fmt.Fprintf(&b, "import upstream %q\n\n", upstream)

	if len(consts) > 0 {
		fmt.Fprintln(&b, "const (")
		for _, name := range consts {
			fmt.Fprintf(&b, "\t%s = upstream.%s\n", name, name)
		}
		fmt.Fprintln(&b, ")")
		fmt.Fprintln(&b)
	}

	for _, name := range typeAliases {
		fmt.Fprintf(&b, "type %s = upstream.%s\n", name, name)
	}
	if len(typeAliases) > 0 {
		fmt.Fprintln(&b)
	}

	if len(vars) > 0 || len(funcs) > 0 {
		fmt.Fprintln(&b, "var (")
		for _, name := range vars {
			fmt.Fprintf(&b, "\t%s = upstream.%s\n", name, name)
		}
		for _, name := range funcs {
			fmt.Fprintf(&b, "\t%s = upstream.%s\n", name, name)
		}
		fmt.Fprintln(&b, ")")
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		log.Fatalf("go/format failed: %v\n\n%s", err, b.String())
	}

	if err := os.WriteFile(out, formatted, 0o644); err != nil {
		log.Fatalf("write %s: %v", out, err)
	}
}

func isExported(name string) bool {
	if name == "" {
		return false
	}
	r := []rune(name)[0]
	return strings.ToUpper(string(r)) == string(r)
}
